	uint32_t constant, grad;//bit数 25
	constant = (1 << 24) | ((uint32_t)getBit64(val, 46, 24) << 1);
	grad = (uint32_t)getBit64(val, 23, 0);
	uint64_t grad2;//bit数 50
	grad2 = (uint64_t)((1 << 24) | (xm << 1)) * (uint64_t)grad;
	uint32_t tmp_m;//25bit
