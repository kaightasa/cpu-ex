uint32_t fsqrt_f(uint32_t x){
	//split sequence to each subsequence
	uint32_t xs, xe, xm;
	xs = getBit32(x, 31, 31);
	xe = getBit32(x, 30, 23);
	xm = getBit32(x, 22, 0);

	//calc s
	uint32_t s = xs;

	uint32_t shift_xe;//8bit
	shift_xe = xe >> 1;

	//calc e
	uint32_t e;//8bit
	e = ((xe & (1 << 0)) != 0) ? (189 -shift_xe) : (190 - shift_xe);

	//calc m
	uint32_t m, key;//bit数23, 11
	uint64_t val;//bit数48
	uint32_t tmpkey = (xe << 10) | getBit32(xm, 22, 13);
	key = getBit32(tmpkey, 10, 0);

	//lookup table and get constant and grad
	val = lookup_table_fsqrt(key);

	uint32_t constant, grad;//bit数 25, 23
	constant = (uint32_t)getBit64(val, 47, 23);
	grad = (uint32_t)getBit64(val, 22, 0);

	uint64_t grad2;//48bit
	grad2 = (uint32_t)((1  << 23) | grad) * (uint64_t)((1 << 23) | xm);

	uint32_t grad3_even;//25bit
	grad3_even = ((grad2 & ((uint64_t)1 << 47)) != 0) ? ((uint32_t)getBit64(grad2, 47, 25)) : ((getBit32(xm, 22, 13) != 0b1111111111) ? ((uint32_t)getBit64(grad2, 46, 24)) : ((uint32_t)getBit64(grad2, 47, 25)));

	uint32_t grad3_odd;//25bit
	grad3_odd = ((grad2 & ((uint64_t)1 << 47)) == 0) ? ((uint32_t)getBit64(grad2, 46, 24)) : ((getBit32(xm, 22, 13) != 0) ? ((uint32_t)getBit64(grad2, 47, 25)) : ((uint32_t)getBit64(grad2, 46, 24)));

	uint32_t tmp_m;//25bit
	tmp_m = ((xe & (1 << 0)) != 0) ? (constant - grad3_odd) : (constant - grad3_even);

	m = getBit32(tmp_m, 22, 0);

	uint32_t tmp_y;//32bit
	uint32_t tmp_x2 = (s << 31) | (e << 23) | m;
	tmp_y = fmul_f(x, tmp_x2);

	uint32_t root2;//32bit
	root2 = 0b00111111101101010000010011110011;

	uint32_t odd_zero_ye;//8bit
	odd_zero_ye = shift_xe + 64;

	uint32_t even_zero_y, even_zero_tmp_ye;//32bit, 8bit
	even_zero_tmp_ye = 63 + shift_xe;
	uint32_t tmp_xx = (xs << 31) | (even_zero_tmp_ye << 23) | xm;
	even_zero_y = fmul_f(root2, tmp_xx);

	uint32_t y;
	y = (xm != 0) ? tmp_y : (((xe & (1 << 0)) != 0) ? ((xs << 31) | (odd_zero_ye << 23) | xm) : even_zero_y);
	return y;
}

